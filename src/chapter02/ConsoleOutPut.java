package chapter02;

public class ConsoleOutPut {
	public static void main(String[] args) {
		System.out.println("안녕");		// println : 프린트 + 줄바꿈(엔터)
		System.out.println("하세요");
		
		System.out.print("안녕  ");
		System.out.print("하세요");		// print : 출력만
		
		System.out.println();
		
		System.out.print("안녕 \n");		// \n : 줄바꿈
		System.out.print("하세요");
	}
}
